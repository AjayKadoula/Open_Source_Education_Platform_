     <h3 dir="ltr"><u>Defence </u></h3>

<p dir="ltr"><strong>AFCAT :</strong> This is an All India level defence recruitment exam to select candidates for Air force. AFCAT is held twice a year,&nbsp;for grant of Short Service Commission in Flying Branch and Permanent/ Short Service Commission in Technical and Ground Duty Branches through Air Force Common Admission Test (AFCAT)&nbsp;</p>
<p dir="ltr">&nbsp;</p>
<p dir="ltr"><strong>UPSC CDS:</strong> The Combined Defence Services Examination (CDSE) is another defence recruitment all India level entrance exam conducted by the Union Public Service Commission. The exam is held twice a year for recruitment into the Indian Military Academy (IMA), Officers Training Academy (OTA), Indian Naval Academy (INA) and Indian Air Force (IAF) Academy.</p>
<p>&nbsp;</p>
<p dir="ltr"><strong>Indian Army University Entry Scheme : &nbsp;</strong></p>
<p>Indian Army recruites unmarried Male B.E/ B.Tech Graduates for Grant of Permanent Commission in the Indian Army for University Entry Scheme (Pre Final year) Course at Indian Military Academy (IMA), Dehradun &amp; Officers Training Academy (OTA), Chennai. Age Limit: 18 to 24&nbsp; years as on&nbsp;01 July&nbsp;of the respective year. The Selection Process includes Screening Test at respective Command HQ. Candidates must be physically fit according to the prescribed physical standards.</p>
<p>&nbsp;</p><hr>
<h3 dir="ltr"><u>Medical</u> </h3>
<p dir="ltr"><strong>NEET: </strong>The National Eligibility- cum- Entrance Test (NEET-PG) is an all India level entrance exam for graduate students who wish to study postgraduate medical course (MD/ MS) in government or private medical colleges of India. It is conducted by the NATBOARD.</p>
<p dir="ltr">&nbsp;</p>
<p dir="ltr"><strong>JIPMER PG Medical Entrance Exam:</strong> Jawaharlal Institute of Post Graduate Medical Education and Research (JIPMER) PG Entrance Exam is conducted for admission to MD / MS course. This medical entrance exam is conducted once in a year.</p>

<p>&nbsp;</p>
<p><strong>PGIMER MD, MS Entrance Exam: Postgraduate Institute of Medical Education &amp; Research (PGIMER), Chandigarh. Doctor of Medicine (MD) and Master of Surgery (MS)</strong>Courses. Admission to various MD/MS courses conducted at the Institute is made twice a year. The admission to the above courses of the Institute is made on merit on all India basis, by holding an entrance exam by the Institute, after issuing a countrywide admission notice. The sessions commence from first January and first July each year The Institute follows a residency service-cum-training scheme. Candidates admitted to MD/MS Courses are called Junior Residents.
</p>
