<h3 dir="ltr"><u>MBA</u></h3>
<p dir="ltr"><strong>Common Admission Test (CAT):</strong> Common Admission Test is conducted by the Indian Institutes of Management (IIMs) every year. It is an all India level entrance exam for admission to the Post-graduate and Fellow Programmes in the management courses offered by the IIMs as well as many management institutes all across the country. The CAT percentage differs from college to college.</p>
<p dir="ltr">&nbsp;</p>
